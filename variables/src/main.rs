const MAX_POINTS: u32 = 100_000;

fn main() {
    let mut x = 5;
    println!("The value of x is: {}", x);

    x = 6;
    println!("The value of MAX is: {}, and for x: {}", MAX_POINTS, x);

    let x = 5;
    let x = x + 1;
    let x = x * 2;

    println!("The value of x is now: {}", x);

    let spaces = "   ";
    let spaces = spaces.len();

    println!("Spaces length: {}", spaces);

}
